// generate-llms-full.mjs — derives site/public/llms-full.txt, the fetch-readable markdown corpus behind
// the docs site's llms.txt index. The site's HTML pages are client-rendered shells (content materializes
// from bundled sources at runtime), so a raw-fetching agent gets no content from them — this file IS the
// content: every component descriptor's prose documentation (the same `{name}.md` files the site's own
// API pages derive from — one fact, one home) plus the root CHANGELOG. Deterministic by construction
// (alphabetical by tag, byte-stable) so the standing drift gate (site/llms.test.ts) can diff a fresh
// generation against the committed file — the gate imports `generateLlmsFull` below, the SAME
// implementation this script runs, so the pair cannot drift. Regenerate: `node scripts/generate-llms-full.mjs`.
//
// Deliberately NOT in the corpus: the guide pages (theming/sizing/forms/…) — their content is TS-authored
// DOM construction with no markdown source; llms.txt marks them as live-demo surfaces. Adding them here
// would mean hand-copying prose that would drift; if they ever gain markdown sources, extend SOURCES.

import { readFileSync, readdirSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { join } from 'node:path'

/** The repo root off this script's own location — LAZY, because `import.meta.url` is only a file: URL
 *  when Node runs the script directly; under the drift gate's Vite transform it is a virtual scheme, and
 *  the gate always passes an explicit `repoRoot` instead (site/lib/llms.test.ts). */
const repoRootFromScript = () => fileURLToPath(new URL('..', import.meta.url))

/** Split a descriptor's `---`-fenced frontmatter from its prose body; null when no fence leads the file. */
function splitFence(source) {
  if (!source.startsWith('---\n')) return null
  const end = source.indexOf('\n---\n', 4)
  if (end === -1) return null
  return { fence: source.slice(4, end), body: source.slice(end + 5) }
}

/** The `tag: ui-…` scalar out of a frontmatter fence; null when absent (a non-descriptor .md). */
function tagOf(fence) {
  const m = fence.match(/^tag:\s*(ui-[a-z-]+)\s*$/m)
  return m ? m[1] : null
}

/** The descriptor-bearing control trees, repo-relative — components' fleet plus the router package's two
 *  elements (ui-router-link / ui-router-outlet live in their own package, same descriptor convention). */
const CONTROL_TREES = [
  'packages/agent-ui/components/src/controls',
  'packages/agent-ui/router/src/controls',
]

/** Build the whole llms-full.txt corpus as one string (pure — the drift gate calls this too). */
export function generateLlmsFull(repoRoot = repoRootFromScript()) {
  const entries = []
  for (const tree of CONTROL_TREES) {
    const controlsDir = join(repoRoot, tree)
    for (const folder of readdirSync(controlsDir, { withFileTypes: true })) {
      if (!folder.isDirectory()) continue
      for (const file of readdirSync(join(controlsDir, folder.name))) {
        if (!file.endsWith('.md')) continue
        const source = readFileSync(join(controlsDir, folder.name, file), 'utf8')
        const split = splitFence(source)
        if (split === null) continue
        const tag = tagOf(split.fence)
        if (tag === null) continue // a .md without a tag: scalar is not a component descriptor
        entries.push({ tag, path: `${tree}/${folder.name}/${file}`, body: split.body.trim() })
      }
    }
  }
  if (entries.length === 0) throw new Error('generate-llms-full: zero descriptors found — the controls glob is broken')
  entries.sort((a, b) => (a.tag < b.tag ? -1 : 1)) // deterministic, byte-stable ordering

  const changelog = readFileSync(join(repoRoot, 'CHANGELOG.md'), 'utf8').trim()

  const parts = [
    '# agent-ui — full markdown corpus',
    '',
    '> The fetch-readable content behind https://…/llms.txt: every ui-* component descriptor\'s prose',
    '> documentation (the canonical per-component source the site\'s own API pages derive from), followed',
    '> by the project changelog. Generated by scripts/generate-llms-full.mjs; a standing drift gate',
    '> (site/llms.test.ts) keeps this file byte-identical to a fresh generation from the sources.',
    '',
    `Components: ${entries.length} descriptors, alphabetical by tag.`,
    '',
  ]
  for (const e of entries) {
    parts.push('---', '', `<!-- source: ${e.path} -->`, '', e.body, '')
  }
  parts.push('---', '', '<!-- source: CHANGELOG.md -->', '', changelog, '')
  return parts.join('\n')
}

// Written only when run directly (`node scripts/generate-llms-full.mjs`) — the drift gate imports the
// function above and never writes.
if (process.argv[1] && import.meta.url === new URL(`file://${process.argv[1]}`).href) {
  const root = repoRootFromScript()
  const out = join(root, 'site/public/llms-full.txt')
  const corpus = generateLlmsFull(root)
  writeFileSync(out, corpus)
  console.log(`llms-full: wrote ${corpus.length} bytes to ${out}`)
}
